package cn.sdu.online.findteam.mob;

public class TeamLogListViewItem {
    public String name;
    public String time;
    public String content;
    public String imgPath;
    public String id;

    public TeamLogListViewItem( String name, String time, String content, String imgPath, String id){
        this.name = name;
        this.time = time;
        this.content = content;
        this.imgPath = imgPath;
        this.id = id;
    }
}
